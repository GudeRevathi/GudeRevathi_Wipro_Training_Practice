public class Simple {
    public static void main(String[] args){
        int n=Integer.parseInt(args[0]);
        switch (n) {
            case 1:
                System.out.println("monday");
                break;
            case 2:
                System.out.println("tuesday");
                break;
            case 3:
                System.out.println("wednesday");
                break;
            default:
                System.out.println("enter a valid data");



        }
    }
}
